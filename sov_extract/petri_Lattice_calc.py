import math

def get_square_coordinates(R=5, n=3, m=5, Delta=0.5):
    """
    Определяет координаты левой нижней точки каждого квадрата решётки.
    
    Параметры:
      R     - Радиус чашки Петри (по умолчанию 5 см).
      n     - Число строк (по умолчанию 3).
      m     - Число столбцов (по умолчанию 5).
      Delta - Отступ вершин решётки от окружности (по умолчанию 0.5 см).
      
    Предполагается, что изображение имеет размер 2R x 2R,
    а система координат имеет начало в левом нижнем углу изображения.
    
    Возвращает:
      squares - Словарь, где ключ (i, j) – индексы строки и столбца,
                а значение – кортеж (x, y) координат левой нижней точки квадрата.
      s       - Вычисленная длина стороны квадрата.
    """
    # Вычисляем сторону квадрата s
    s = 2 * (R - Delta) / math.sqrt(m**2 + n**2)
    
    # Координаты нижнего левого угла решётки (она симметрично расположена относительно центра)
    x0 = R - (m * s) / 2
    y0 = R - (n * s) / 2

    squares = {}
    for i in range(n):        # i = 0 (нижняя строка) до n-1 (верхняя)
        for j in range(m):    # j = 0 (самый левый столбец) до m-1 (самый правый)
            x = x0 + j * s
            y = y0 + i * s
            squares[(i, j)] = (x, y)
    
    return squares, s

# Пример использования:
if __name__ == "__main__":
    squares, s = get_square_coordinates()
    print(f"Сторона квадрата: {s:.3f} см")
    for key in sorted(squares.keys()):
        print(f"Квадрат {key}: нижний левый угол в точке {squares[key]}")
